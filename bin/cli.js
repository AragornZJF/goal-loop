#!/usr/bin/env node
'use strict';

// goal-loop CLI — 入口
// 子命令：
//   goal-loop init [target-dir] [--force]   生成 .claude/skills/goal-loop/ + loop.sh + clean-state.sh
//   goal-loop build [N]                      透传到 ./loop.sh build（Phase 3 无头构建循环）
//   goal-loop plan  [N]                      透传到 ./loop.sh plan（Phase 2 无头规划循环）
//   goal-loop clean [--confirm]              透传到 ./clean-state.sh
//   goal-loop --help | -h                    帮助
//   goal-loop --version | -v                 版本

const fs = require('fs');
const path = require('path');
const { spawn } = require('child_process');

const PKG_DIR = path.resolve(__dirname, '..');
const SKILL_SRC = path.join(PKG_DIR, 'skill');
const PKG_JSON = JSON.parse(fs.readFileSync(path.join(PKG_DIR, 'package.json'), 'utf8'));

// ─── wrapper 模板（项目级优先、回退用户级，与 skill 内置 wrapper 一致） ───
const LOOP_WRAPPER = `#!/bin/bash
# Generated by goal-loop CLI. 项目级优先，回退用户级。
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
if [ -x "$SCRIPT_DIR/.claude/skills/goal-loop/scripts/loop.sh" ]; then
    exec "$SCRIPT_DIR/.claude/skills/goal-loop/scripts/loop.sh" "$@"
else
    exec "$HOME/.claude/skills/goal-loop/scripts/loop.sh" "$@"
fi
`;

const CLEAN_WRAPPER = `#!/bin/bash
# Generated by goal-loop CLI. 项目级优先，回退用户级。
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
if [ -x "$SCRIPT_DIR/.claude/skills/goal-loop/scripts/clean-state.sh" ]; then
    exec "$SCRIPT_DIR/.claude/skills/goal-loop/scripts/clean-state.sh" "$@"
else
    exec "$HOME/.claude/skills/goal-loop/scripts/clean-state.sh" "$@"
fi
`;

// ─── 工具函数 ───
function log(msg) { process.stdout.write(msg + '\n'); }
function warn(msg) { process.stderr.write('⚠️  ' + msg + '\n'); }

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

// 递归拷贝目录；对已存在目标文件，skipExisting=true 则跳过，否则覆盖
function copyDir(src, dest, skipExisting, relPrefix) {
  const prefix = relPrefix || '';
  let created = 0;
  let skipped = 0;
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const s = path.join(src, entry.name);
    const d = path.join(dest, entry.name);
    const rel = prefix ? prefix + '/' + entry.name : entry.name;
    if (entry.isDirectory()) {
      ensureDir(d);
      const r = copyDir(s, d, skipExisting, rel);
      created += r.created;
      skipped += r.skipped;
    } else {
      if (skipExisting && fs.existsSync(d)) {
        warn('跳过已存在：' + rel);
        skipped++;
      } else {
        ensureDir(path.dirname(d));
        fs.copyFileSync(s, d);
        created++;
      }
    }
  }
  return { created, skipped };
}

// 递归删除目录（用于 --force 清理 skill 目标）
function rmrf(dir) {
  if (fs.existsSync(dir)) {
    fs.rmSync(dir, { recursive: true, force: true });
  }
}

// ─── init ───
function cmdInit(args) {
  const force = args.includes('--force') || args.includes('-f');
  const target = args.filter(a => !a.startsWith('-'))[0] || process.cwd();
  const targetAbs = path.resolve(target);

  if (!fs.existsSync(SKILL_SRC)) {
    process.stderr.write('错误：包内 skill/ 资源缺失（' + SKILL_SRC + '）。包可能已损坏。\n');
    process.exit(1);
  }

  const skillDest = path.join(targetAbs, '.claude', 'skills', 'goal-loop');

  log('目标目录：' + targetAbs);
  log('');

  // 1. 铺 skill 资源
  if (force) rmrf(skillDest);
  ensureDir(skillDest);
  log('▸ 写入 skill 到 .claude/skills/goal-loop/');
  const r = copyDir(SKILL_SRC, skillDest, !force);
  log('  写入 ' + r.created + ' 个文件' + (r.skipped ? '，跳过 ' + r.skipped + ' 个已存在文件' : ''));
  if (force) log('  （--force 模式：已覆盖目标）');

  // 2. 生成 wrapper 脚本
  log('▸ 生成 wrapper 脚本');
  writeWrapper(path.join(targetAbs, 'loop.sh'), LOOP_WRAPPER, force);
  writeWrapper(path.join(targetAbs, 'clean-state.sh'), CLEAN_WRAPPER, force);

  log('');
  log('✅ init 完成。');
  log('');
  log('下一步：');
  log('  1. 在 Claude Code 会话里用触发词（如「探索需求」）激活 skill；');
  log('     skill 会根据文件状态自动路由到 Phase 1/2/3。');
  log('  2. 或直接启动循环（需已安装 claude CLI，建议在隔离环境运行）：');
  log('       goal-loop build 20     # Phase 3 无头构建，最多 20 轮');
  log('       goal-loop plan 5       # Phase 2 无头规划，最多 5 轮');
  log('  3. 首次运行 ./loop.sh 会自动创建 AGENTS.md / build/ / specs/。');
  log('');
  log('⚠️  loop.sh / clean-state.sh 为 bash 脚本，需 Git Bash / WSL / macOS / Linux。');
  log('⚠️  循环以 claude --dangerously-skip-permissions 运行，请在隔离环境中使用。');
}

function writeWrapper(file, content, force) {
  if (!force && fs.existsSync(file)) {
    warn('跳过已存在：' + path.basename(file) + '（用 --force 覆盖）');
    return;
  }
  fs.writeFileSync(file, content, { mode: 0o755 });
  // Windows 上 chmod 不生效，尝试但不报错
  try { fs.chmodSync(file, 0o755); } catch (_) {}
  log('  ✓ ' + path.basename(file));
}

// ─── 透传到 ./loop.sh 或 ./clean-state.sh ───
function spawnWrapper(scriptName, forwardArgs, modeLabel) {
  const cwd = process.cwd();
  const script = path.join(cwd, scriptName);
  if (!fs.existsSync(script)) {
    process.stderr.write(
      '错误：当前目录未找到 ' + scriptName + '。\n' +
      '请先在本项目运行：goal-loop init\n'
    );
    process.exit(1);
  }

  // 优先用 bash 执行（Windows 下需 Git Bash）；找不到 bash 则直接 spawn 脚本
  const shell = process.env.SHELL || (process.platform === 'win32' ? 'bash' : '/bin/bash');
  let child;
  const tryBash = () => {
    child = spawn(shell, [script, ...forwardArgs], { stdio: 'inherit' });
  };

  try {
    tryBash();
  } catch (e) {
    // shell 不存在，直接执行脚本（依赖系统关联）
    child = spawn(script, forwardArgs, { stdio: 'inherit', shell: false });
  }

  child.on('error', (err) => {
    // bash 启动失败（如 Windows 未装 Git Bash），退化为直接执行
    if (err.code === 'ENOENT') {
      process.stderr.write(
        '⚠️  无法启动 bash（' + shell + '）。请确认已安装 Git Bash / WSL。\n' +
        '   或手动运行：bash ' + scriptName + ' ' + forwardArgs.join(' ') + '\n'
      );
      process.exit(127);
    } else {
      process.stderr.write('错误：' + err.message + '\n');
      process.exit(1);
    }
  });

  child.on('close', (code) => {
    process.exit(code === null ? 1 : code);
  });
}

function cmdBuild(args) { spawnWrapper('loop.sh', ['build', ...args], 'build'); }
function cmdPlan(args) { spawnWrapper('loop.sh', ['plan', ...args], 'plan'); }
function cmdClean(args) { spawnWrapper('clean-state.sh', args, 'clean'); }

// ─── help / version ───
function cmdHelp() {
  log([
    'goal-loop ' + PKG_JSON.version,
    '',
    '从澄清需求、规划到自动化构建的 Claude Code skill 流水线 CLI。',
    '',
    '用法：',
    '  goal-loop                                 一键安装：默认在当前目录初始化 skill',
    '  goal-loop init [target-dir] [--force]   生成 skill + wrapper 脚手架（默认当前目录）',
    '  goal-loop build [N]                      Phase 3：无头构建循环（透传到 ./loop.sh build）',
    '  goal-loop plan  [N]                      Phase 2：无头规划循环（透传到 ./loop.sh plan）',
    '  goal-loop clean [--confirm]              清空项目状态（透传到 ./clean-state.sh）',
    '  goal-loop --help, -h                     显示本帮助',
    '  goal-loop --version, -v                  显示版本',
    '',
    '示例：',
    '  npx goal-loop              # 一键安装（推荐）',
    '  goal-loop init .           # 在当前目录初始化',
    '  goal-loop build 20         # 最多构建 20 轮',
    '  goal-loop plan 5           # 最多规划 5 轮',
    '',
    '说明：',
    '  - build/plan/clean 需先 init，且需已安装 claude CLI（Claude Code）。',
    '  - loop.sh / clean-state.sh 为 bash 脚本，需 Git Bash / WSL / macOS / Linux。',
    '  - 循环以 claude --dangerously-skip-permissions 运行，请在隔离环境中使用。',
    '',
  ].join('\n'));
}

function cmdVersion() {
  log(PKG_JSON.version);
}

// ─── 路由 ───
function main(argv) {
  const args = argv.slice(2);
  const first = args[0];

  // 保留 --help / --version 的显式响应
  if (first === '--help' || first === '-h' || first === 'help') { return cmdHelp(); }
  if (first === '--version' || first === '-v' || first === 'version') { return cmdVersion(); }

  // npx goal-loop 一键安装：无子命令 或 以 flag 开头（如 --force）时默认执行 init
  if (args.length === 0 || (first && first.startsWith('-'))) {
    log('未指定子命令，默认在当前目录初始化 skill...\n');
    return cmdInit(args);
  }

  const cmd = args[0];
  const rest = args.slice(1);

  switch (cmd) {
    case 'init': return cmdInit(rest);
    case 'build': return cmdBuild(rest);
    case 'plan': return cmdPlan(rest);
    case 'clean': return cmdClean(rest);
    default:
      process.stderr.write('未知命令：' + cmd + '\n运行 goal-loop --help 查看可用命令。\n');
      process.exit(1);
  }
}

main(process.argv);
